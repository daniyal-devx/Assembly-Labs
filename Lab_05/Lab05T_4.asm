.MODEL SMALL
 .STACK 100H
 .CODE
 MAIN PROC
 MOV AX, @DATA
 MOV DS, AX
 CALL TAKE_INPUT 
MOV DL, AL
 CALL  GIVE_OUTPUT
 MOV AH, 4CH 
INT 21H
 MAIN ENDP
 TAKE_INPUT PROC
 MOV AH, 01
 INT 21H
 RET
 TAKE_INPUT  ENDP
 GIVE_OUTPUT PROC
 MOV AH, 02
 INT 21
 GIVE_OUTPUT ENDP
 .DATA 
VAR1 DB 23, 45, 56, 67
 VAR2 DB 89, 34, 67, 56
 END MAIN